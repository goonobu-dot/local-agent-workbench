#!/usr/bin/env bash
set -euo pipefail

LOOPS_FILE="${1:-docs/continuous-improvement-loops.md}"
LOG_FILE="${2:-docs/continuous-improvement-run-log.md}"

mkdir -p "$(dirname "$LOOPS_FILE")" "$(dirname "$LOG_FILE")"

cat >"$LOOPS_FILE" <<'EOF'
# Continuous Improvement Loops

This file is generated by `scripts/run_50_improvement_loops.sh`.
Each row was added in a separate loop, then verified immediately by
`scripts/check_continuous_improvement_loops.sh`.

| Loop | Improvement Added | Immediate Test |
| --- | --- | --- |
EOF

cat >"$LOG_FILE" <<'EOF'
# Continuous Improvement Run Log

EOF

improvements=(
"Clarified that first-time readers should start with install-free evaluation before installing."
"Added a stronger rule that every public workflow artifact should be inspectable before sharing."
"Documented that setup diagnostics are useful even when the CLI dependency is missing."
"Reinforced that uninstall should be reversible unless explicit removal flags are set."
"Highlighted that issue triage is the safest default workflow for unfamiliar tasks."
"Recorded that security triage should start from impact and reproduction boundaries."
"Recorded that documentation improvement starts from the confused reader, not the writer."
"Recorded that dependency update review should separate compatibility, tests, and rollback risk."
"Added guidance that URL-created workflows should keep canonical source links."
"Added guidance that GitHub metadata is helpful but should not be required for offline use."
"Added a reminder that workflow exports should be reviewed before publication."
"Added a reminder that import paths must stay inside the destination directory."
"Added a reminder that doctor reports should be reviewed before issue submission."
"Added a note that pane roles should create independent angles rather than duplicate work."
"Added a note that handoff summaries should name unresolved questions explicitly."
"Added a note that demo workflows should use fictional data only."
"Added a note that README examples should link to final artifacts, not only folders."
"Added a note that quick-start commands should be copy-pasteable."
"Added a note that advanced commands should live outside the first screen."
"Added a note that CI should run the same checks maintainers run locally."
"Added a note that release notes should explain user-visible changes plainly."
"Added a note that support docs should route users to diagnostics first."
"Added a note that FAQ entries should answer security concerns directly."
"Added a note that workflow templates should include the decision they produce."
"Added a note that example outputs should show a recommendation, not just raw notes."
"Added a note that release preparation should include follow-up risks."
"Added a note that PR review should separate behavior, tests, safety, and docs."
"Added a note that issue triage should separate reproduction, code path, fix, and response."
"Added a note that feature discovery should end with scope boundaries."
"Added a note that local-only tools should state what never leaves the machine."
"Added a note that manual clone is the trust-building path for cautious users."
"Added a note that one-command install is the speed path for confident users."
"Added a note that demo transcript helps users understand output before running code."
"Added a note that workflow recommendation helps users choose the first action."
"Added a note that scorecards should be tested for count and endpoints."
"Added a note that generated docs should be reproducible by script."
"Added a note that scripts should fail closed for unsafe sharing operations."
"Added a note that noncritical setup checks should not block demo exploration."
"Added a note that public examples should avoid real client or project data."
"Added a note that command references should include recovery commands."
"Added a note that adoption plans should avoid fake metrics."
"Added a note that showcase docs should explain the boundary of the tool."
"Added a note that architecture docs should identify shared state."
"Added a note that troubleshooting docs should include environment variables."
"Added a note that workflow sharing docs should explain archive contents."
"Added a note that validation docs should include what a passing result means."
"Added a note that generated reports should preserve raw output and suggested fixes."
"Added a note that install docs should include uninstall instructions."
"Added a note that tests should check reader-facing docs as first-class behavior."
"Added a final note that repeated improvement loops should leave a verifiable audit trail."
)

for index in "${!improvements[@]}"; do
  loop_number=$((index + 1))
  printf -v loop_label 'Loop %02d' "$loop_number"
  improvement="${improvements[$index]}"
  printf '| %s | %s | `scripts/check_continuous_improvement_loops.sh %s %02d` |\n' \
    "$loop_label" "$improvement" "$LOOPS_FILE" "$loop_number" >>"$LOOPS_FILE"
  ./scripts/check_continuous_improvement_loops.sh "$LOOPS_FILE" "$loop_number" >/dev/null
  printf '%s\n' "- $loop_label: PASS - $improvement" >>"$LOG_FILE"
done

echo "Completed 50 add-and-test improvement loops"
